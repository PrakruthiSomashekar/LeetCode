# There are n soldiers standing in a line. Each soldier is assigned a unique rating value.
# Form a team of 3 soldiers where  (rating[i] < rating[j] < rating[k]) or (rating[i] > rating[j] > rating[k]) where (0 <= i < j < k < n).

from itertools import combinations
import time

class Solution(object):
    def numTeams(self, rating):
        comb = combinations(rating,3)
        count = 0
        start = time.time()
        for i in list(comb):
            if list(i)==sorted(list(i)) or list(i)==sorted(list(i),reverse=True):
                count += 1
        end = time.time()
        print('sorted elapsed: {}'.format(end - start))

        return count

